package Bundle::Markup;

$VERSION = '0.1';

__END__

=head1 NAME

Bundle::Markup - A bundle to install the Markup distrobution

=head1 SYNOPSIS

C<perl -MCPAN -e 'install Bundle::Markup'>

=head1 CONTENTS

LWP::Simple		- prerequisite

HTML::Parser            - prerequisite
HTML::TreeBuilder       - prerequisite

Bundle::XM:		- prerequisite

Markup::TreeNode	- part of the Markup distrobution
Markup::Tree		- part of the Markup distrobution
Markup::MatchNode	- part of the Markup distrobution
Markup::MatchTree	- part of the Markup distrobution
Markup::Content		- part of the Markup distrobution

=head1 DESCRIPTION

This bundle gathers together all of the prerequisite modules for
Markup::*, as well as the Markup distrobution.

